mapping query_max_stats(string class, string gender)
{
  //not going to bother putting every non-promoted unit into the mapping
  if(!member_array(class, max_stats))
  {
    return max_stats["unpromoted"];	  
  }
  else if(max_stats[class][gender])
  {
    return max_stats[class][gender];	  
  }
  else if(class == "warrior" && gender != "morph")
  {
    return max_stats["warrior"]["normal"];	  
  }
  else
  {
    return max_stats[class];
  }	
}